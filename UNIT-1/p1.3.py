''' P-1.3 :Write a Python program to prompt users to enter a distance in kilometers;
then convert kilometers to miles,
where 1 kilometer is equal to 0.62137 miles. Display the result.'''


km = float (input ("Please enter the speed of car in Kilometre as a unit: "))  

miles=(km * 0.62137)


print ("The speed value of car in Miles: ", miles)  
